package com.example.lr3_2.model

/**
 * Перечисление [FuelType] описывает все доступные виды топлива,
 * представленные на АЗС, с указанием отображаемого названия
 * и стоимости за литр (или эквивалент для электроэнергии).
 *
 * Используется для отображения в UI и расчёта выручки.
 *
 * @property displayName Название топлива, отображаемое пользователю.
 * @property pricePerLiter Стоимость одного литра (или кВт⋅ч) топлива.
 */
enum class FuelType(val displayName: String, val pricePerLiter: Double) {
    AI92("АИ-92", 60.39),
    AI95("АИ-95", 64.89),
    AI98("АИ-98", 90.13),
    AI100("АИ-100", 81.15),
    DIESEL("Дизель", 72.11),
    DIESEL_ARCTIC("Дизель (Арктика)", 65.0),
    GAS_PROPANE("Газ (Пропан-Бутан)", 31.0),
    GAS_METHANE("Газ (Метан)", 31.41),
    ELECTRIC("Электроэнергия (кВт⋅ч)", 20.0);

    companion object {
        /**
         * Возвращает вид топлива, используемый по умолчанию
         * при открытии экрана нового заказа.
         *
         * @return [FuelType.AI95] — стандартное топливо по умолчанию.
         */
        fun getDefault() = AI95
    }
}
